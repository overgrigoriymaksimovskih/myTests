package LyambdyAndAnonimusClasses;

public class MyClass {
    public MyClass(){
        System.out.println("Создан объект класса \"MyClass\"");
    }
    public static void staticMethod (){
        System.out.println("Вызван статический метод класса \"MyClass\"");
    }
    public void objectsMethod (){
        System.out.println("Вызван метод объекта \"MyClass\"");
    }
}
